package com.example.lesson01;

public class Data {

	private int id;
	private String name;
	
	// private니까 getter,setter 필요하다!
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	
	
}
